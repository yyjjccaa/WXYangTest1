const util = require('../../utils/util.js')

Page({
  data: {
    logs: []
  },
  onLoad: function () {
    this.setData({
      logs: (wx.getStorageSync('logs') || []).map(log => {
        return util.formatTime(new Date(log))
      })
    })
  },
  onPageScroll: function (obj) {
    // Do something when page scroll
    console.log("Yang", obj.scrollTop)

  },
})
